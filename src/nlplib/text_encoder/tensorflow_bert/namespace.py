class BertHandleNotFound(Exception):
    def __init__(self, bert_handle_name: str) -> None:
        super().__init__(
            f"{bert_handle_name} not found, must be in:\n" + \
            f"{[*MAP_HANDLE_TO_ENCODER.keys()]}"
        )

def clear_temp_tfhub_modules() -> None:
    # Remove temporary downloaded directories and files
    temp_dpath = os.path.join(os.getenv("LOCALAPPDATA"), "Temp", "tfhub_modules")

    if os.path.exists(temp_dpath):
        shutil.rmtree(temp_dpath)

def get_bert_encoder(bert_handle_name: str, **kwargs) -> hub.KerasLayer:
    if bert_handle_name not in MAP_HANDLE_TO_ENCODER:
        raise BertHandleNotFound(bert_handle_name)

    try: # Previously stored temp files may raise exceptions
        return hub.KerasLayer(MAP_HANDLE_TO_ENCODER[bert_handle_name], **kwargs)
    except:
        clear_temp_tfhub_modules()
        return hub.KerasLayer(MAP_HANDLE_TO_ENCODER[bert_handle_name], **kwargs)

def get_bert_preprocessor(bert_handle_name: str, **kwargs) -> hub.KerasLayer:
    if bert_handle_name not in MAP_HANDLE_TO_PREPROCESSOR:
        raise BertHandleNotFound(bert_handle_name)
    
    try: # Previously stored temp files may raise exceptions
        return hub.KerasLayer(MAP_HANDLE_TO_PREPROCESSOR[bert_handle_name], **kwargs)
    except:
        clear_temp_tfhub_modules()
        return hub.KerasLayer(MAP_HANDLE_TO_PREPROCESSOR[bert_handle_name], **kwargs)

